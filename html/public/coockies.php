  <h1>Cookies essentiels</h1>
  <p>
    Ils sont strictement nécessaires à l’utilisation du service et permettent
    l’utilisation des principales fonctionnalités du site, comme le cas
    échéant l’accès à votre compte personnel, ou encore de mémoriser les
    préférences d'affichage de votre terminal (langue, paramètres d'affichage)
    et d'en tenir compte lors de vos visites, selon la charte graphique et les
    logiciels de visualisation ou de lecture de votre terminal. Ils peuvent
    inclure des cookies de réseaux sociaux lorsque vous interagissez avec ces
    derniers. Ils nous permettent aussi de lier entre elles les différentes
    pages consultées pour vous assurer une navigation fluide.<br />
    Pour paramétrer :<br />
    Vous pouvez désactiver complètement les cookies dans votre navigateur.
    Dans ce cas notre site ne fonctionnera plus normalement mais vous pourrez
    quand même effectuer des actions basiques.<br />
  </p>
  <h4>1/ si vous utilisez le navigateur Internet Explorer</h4>
  <ul>
    <li>
      Dans Internet Explorer, cliquez sur le bouton « Outils », puis sur «
      Options Internet
    </li>
    <li>
      Sous l’onglet Confidentialité déplacez le curseur vers le haut pour
      bloquer tous les cookies ou vers le bas pour autoriser tous les cookies,
      puis cliquez sur OK.
    </li>
  </ul>
  <h4>2/ si vous utilisez le navigateur Firefox</h4>
  <ul>
    <li>
      Allez dans le menu « Outils » du navigateur puis sélectionnez le menu «
      Options »
    </li>
    <li>
      Cliquez sur l’onglet « vie privée », décochez la case « Accepter les
      cookies » puis cliquez sur OK.
    </li>
  </ul>
  <h4>3/ si vous utilisez le navigateur Safari</h4>
  <ul>
    <li>
      Dans votre navigateur, choisissez le menu « Édition » puis sélectionnez
      « Préférences ».
    </li>
    <li>Cliquez sur « Confidentialité ».</li>
    <li>
      Positionnez le réglage « Bloquer les cookies » sur « Toujours » puis
      cliquez sur OK
    </li>
  </ul>
  <h4>4/ si vous utilisez le navigateur Google Chrome</h4>
  <ul>
    <li>Cliquez sur le menu Chrome dans la barre d'outils du navigateur.</li>
    <li>Sélectionnez « Paramètres ».</li>
    <li>Cliquez sur « Afficher les paramètres avancés ».</li>
    <li>
      Dans la section « Confidentialité », cliquez sur le bouton « Paramètres
      de contenu ».
    </li>
    <li>
      Dans la section « Cookies », sélectionnez « Interdire à tous les sites
      de stocker des données » et cochez la case « Bloquer les cookies et les
      données de site tiers », puis cliquez sur OK.
    </li>
  </ul>
